function getLang() {
  return localStorage.getItem('ex3m_lang') || 'ru';
}

function setLang(lang) {
  localStorage.setItem('ex3m_lang', lang);
  applyLang();
}

function t(key) {
  const lang = getLang();
  if (lang === 'ru') return null;
  return (I18N[lang] && I18N[lang][key]) || null;
}

function applyLang() {
  const lang = getLang();

  document.querySelectorAll('[data-i18n]').forEach(el => {
    if (!el.dataset.i18nOrig) el.dataset.i18nOrig = el.textContent;
    const key = el.getAttribute('data-i18n');
    const val = (lang !== 'ru' && I18N[lang] && I18N[lang][key]) || el.dataset.i18nOrig;
    el.textContent = val;
  });

  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    if (!el.dataset.i18nPhOrig) el.dataset.i18nPhOrig = el.placeholder;
    const key = el.getAttribute('data-i18n-ph');
    const val = (lang !== 'ru' && I18N[lang] && I18N[lang][key]) || el.dataset.i18nPhOrig;
    el.placeholder = val;
  });

  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    if (!el.dataset.i18nHtmlOrig) el.dataset.i18nHtmlOrig = el.innerHTML;
    const key = el.getAttribute('data-i18n-html');
    const val = (lang !== 'ru' && I18N[lang] && I18N[lang][key]) || el.dataset.i18nHtmlOrig;
    el.innerHTML = val;
  });

  document.querySelectorAll('.lang-btn').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === lang);
  });

  if (typeof onLangChange === 'function') onLangChange();
}

document.addEventListener('DOMContentLoaded', applyLang);
